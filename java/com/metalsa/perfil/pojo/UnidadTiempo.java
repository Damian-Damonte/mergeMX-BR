public class UnidadTiempo {

    private Long bloque;
    private Long idUnidadTiempo;
    private String descripcion;

    public Long getBloque() {
        return bloque;
    }

    public void setBloque(Long bloque) {
        this.bloque = bloque;
    }

    public Long getIdUnidadTiempo() {
        return idUnidadTiempo;
    }

    public void setIdUnidadTiempo(Long idUnidadTiempo) {
        this.idUnidadTiempo = idUnidadTiempo;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
}
